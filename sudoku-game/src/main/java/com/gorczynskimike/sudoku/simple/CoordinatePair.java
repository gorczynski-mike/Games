package com.gorczynskimike.sudoku.simple;

public class CoordinatePair {

    private int x;
    private int y;

    public CoordinatePair(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

}